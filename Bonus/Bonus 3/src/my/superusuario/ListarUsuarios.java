    public void exibeInformacoes(){

        System.out.println(sis.db.size());
        
        for(int i=0; i < sis.db.size(); i++){ 
            System.out.println("Entreiii ");
            
            System.out.println(i);
            
            Tabela.setValueAt(i+1, i, 0);
            
            Tabela.setValueAt(sis.db.get(i).getNome(), i, 1);
            Tabela.setValueAt(sis.db.get(i).getCpf(), i, 2);
            Tabela.setValueAt(sis.db.get(i).getEmail(), i, 3);
            Tabela.setValueAt(sis.db.get(i).getData(), i, 4);
            Tabela.setValueAt(sis.db.get(i).getSenha(), i, 5);
            
            
            
        }
            
    }
    
